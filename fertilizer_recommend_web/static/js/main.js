document.getElementById('assessmentForm').addEventListener('submit', async function(e) {
    e.preventDefault();
    
    const formData = new FormData(this);
    const submitButton = this.querySelector('button[type="submit"]');
    const originalButtonText = submitButton.textContent;
    submitButton.disabled = true;
    submitButton.textContent = 'Processing...';
    
    try {
        const response = await fetch('/predict/', {
            method: 'POST',
            body: formData,
            headers: {
                'X-CSRFToken': document.querySelector('[name=csrfmiddlewaretoken]').value
            }
        });
        
        const data = await response.json();
        
        // Check if response was successful
        if (data.success === false) {
            displayError(data.error || 'An error occurred while processing your assessment.');
        } else {
            // Display results in the card
            displayResult(data);
        }
    } catch (error) {
        console.error('Error:', error);
        displayError('An error occurred while processing your assessment. Please try again.');
    }finally{
        submitButton.disabled = false;
        submitButton.textContent = originalButtonText;
    }
});

function displayResult(data) {
    const resultCard = document.getElementById('resultCard');
    const predictionResult = document.getElementById('predictionResult');
    const confidenceBar = document.getElementById('confidenceBar');
    const confidenceText = document.getElementById('confidenceText');
    const resultMessage = document.getElementById('resultMessage');
    const llmAdviceBlock = document.getElementById('llmAdviceBlock');
    const llmAdvice = document.getElementById('llmAdvice');
    const llmStatus = document.getElementById('llmStatus');
    
    // Remove error class if present
    resultCard.classList.remove('error');
    
    // Reset confidence bar first
    confidenceBar.style.width = '0%';
    
    // Set prediction value
    if (data.prediction) {
        predictionResult.textContent = data.prediction;
    }
    
    // Set confidence level
    if (data.confidence !== undefined) {
        const confidence = Math.round(data.confidence * 100);
        confidenceText.textContent = confidence + '%';
        // Animate the confidence bar
        setTimeout(() => {
            confidenceBar.style.width = confidence + '%';
        }, 100);
    }
    
    // Set result message
    if (data.message) {
        resultMessage.textContent = data.message;
    } else {
        resultMessage.textContent = 'Your assessment has been processed successfully.';
    }
    // set llm advice
    if (data.llm_advice) {
        llmAdvice.innerHTML = formatAdviceText(data.llm_advice);
        llmAdviceBlock.classList.remove('hidden');
        llmStatus.textContent = getLlmStatusText(data.llm_status, data.llm_error);
    } else {
        llmAdvice.innerHTML = '';
        llmStatus.textContent = '';
        llmAdviceBlock.classList.add('hidden');
    }
    
    // Show the result card
    resultCard.classList.remove('hidden');
    
    // Scroll to result card
    resultCard.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}

function displayError(message) {
    const resultCard = document.getElementById('resultCard');
    const predictionResult = document.getElementById('predictionResult');
    const confidenceBar = document.getElementById('confidenceBar');
    const resultMessage = document.getElementById('resultMessage');
    const llmAdviceBlock = document.getElementById('llmAdviceBlock');
    
    // Add error class
    resultCard.classList.add('error');
    
    // Set error message
    predictionResult.textContent = 'Error';
    resultMessage.textContent = message;
    
    // Reset confidence bar
    confidenceBar.style.width = '0%';
    document.getElementById('confidenceText').textContent = '0%';
    llmAdviceBlock.classList.add('hidden');
    
    // Show the result card
    resultCard.classList.remove('hidden');
    
    // Scroll to result card
    resultCard.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}

function closeResultCard() {
    const resultCard = document.getElementById('resultCard');
    resultCard.classList.add('hidden');
}

function getLlmStatusText(status, error) {
    if (status === 'generated') {
        return 'Generated by Gemini.';
    }

    if (status === 'disabled') {
        return 'Gemini is not configured. Showing the default recommendation message.';
    }

    if (status === 'unavailable') {
        return 'Install google-genai to enable Gemini advice.';
    }

    if (status === 'failed') {
        return error ? 'Gemini request failed: ' + error : 'Gemini request failed.';
    }

    if (status === 'empty') {
        return 'Gemini returned no text. Showing fallback advice.';
    }

    return '';
}
// ================ Format the LLM advice text ==============
function formatAdviceText(text) {
    return text
        .split(/\n+/)
        .map((line) => line.trim())
        .filter(Boolean)
        .map((line) => {
            const cleanLine = line
                .replace(/\*\*/g, '')
                .replace(/^\d+\.\s*/, '');

            if (/^(Recommendation|Why this fits|Practical caution)\b:?/i.test(cleanLine)) {
                return '<h3>' + escapeHtml(cleanLine.replace(/:$/, '')) + '</h3>';
            }

            return '<p>' + escapeHtml(cleanLine) + '</p>';
        })
        .join('');
}

function escapeHtml(value) {
    return value.replace(/[&<>"']/g, (character) => {
        const entities = {
            '&': '&amp;',
            '<': '&lt;',
            '>': '&gt;',
            '"': '&quot;',
            "'": '&#39;'
        };

        return entities[character];
    });
}
